import { ConnectButton } from '@rainbow-me/rainbowkit';
import type { NextPage } from 'next';
import Head from 'next/head';
import styles from '../styles/Home.module.css';
import EPSButton from '../components/EPSButton'
import { useState, useEffect } from "react";
import { useContractRead, useAccount } from "wagmi";
import EPSAPI from '../contract/abi.json'

import { useRouter } from "next/router";


const Home: NextPage = () => {
  const jwt = require("jsonwebtoken");
  const { address, isConnecting, isDisconnected } = useAccount();
  const [addy, setHotWallet] = useState<any[]>([]);
  const router = useRouter();
  const [token, setToken] = useState<any[]>([]);
  const [decoded, setDecoded] = useState(null);
  const [errosr, setErrosr] = useState<any[]>([]);

//get values from URL
useEffect(() => {
  const token: any = router.query.token;
  setToken(token) ;

  if (token) {
    try {
      const decoded = jwt.verify(token, "secret");
      setDecoded(decoded);
    } catch (error) {
      setErrosr(error as any);
    }
  } else {
    setErrosr("No token found in URL." as any);
  }

  console.log("DECODED TOKEN🌸🌸🌸", decoded)
}, [router.query]);

// set the users hot wallet using RAINBOW
  useEffect(() => {
    if (address) {
      setHotWallet(address as any);
    }
  }, [address]);


//set the users cold wallet using EPS
const { data, isError, isLoading, error } = useContractRead({
  address: "0x0000000000000aF8FE6E4DE40F4804C90fA8Ea8F",
  abi: EPSAPI,
  functionName: "hotToRecord",
  args: [address],
});





//send data to db
useEffect(() => {
  if (address) {
    setHotWallet(address as any);

   
  }
}, [address, data]);

  return (
    <div className={styles.container}>
      <Head>
        <title>RainbowKit App 🌸🌸🌸</title>
        <meta
          name="description"
          content="Generated by @rainbow-me/create-rainbowkit"
        />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main} suppressHydrationWarning >
        <ConnectButton />
HOT WALLET: {addy} <br></br>
{data && (data as any).cold && <p>EPS Connected COLD WALLET: {(data as any).cold}</p>}
       

        <h1 className={styles.title}>
          Welcome to <a href="">Wassie</a><a href="">BATTLES 🌸🌸🌸</a> +{' '}
         
        </h1>
        <div>
      {token && <p>Token: {token}</p>}
      {decoded && <p>Decoded payload: {JSON.stringify(decoded)}</p>}
      {error && <p>Error: {error.message}</p>}
    </div>
       
      </main>

      <footer className={styles.footer}>
        <a href="https://blossomdao.space" target="_blank" rel="noopener noreferrer">
          Made with ❤️ by your frens at BlossomDAO
        </a>
      </footer>
    </div>
  );
};

export default Home;
